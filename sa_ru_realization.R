# ============================================================================
# КОД ДЛЯ ИСПОЛЬЗОВАНИЯ СЕЗОННОЙ ФУНКЦИИ
# Перед запуском необходимо установить рабочую директорию через "Session"
# ============================================================================

library(readxl)
library(dplyr)
library(ggplot2)

# Загружаем функцию
source("sa_ru_functions_v2.R")

# Путь к производственному календарю
calendar_path <- "russia_calendar.xlsx"

# Загружаем ряд. !!! Колонки должны называться date и value.
# Даты понимаются в любом из форматов: 2015M1, 2015-01, datetime, и т.д.
my_data <- readxl::read_xlsx("inflprod_nonSA.xlsx") # <- название файла


# ============================================================================
# 1. ОБЫЧНАЯ СЕЗОННАЯ КОРРЕКТИРОВКА
# Параметры можно оставить как есть, а можно настроить (см. комментарии)
# ============================================================================

res <- sa_ru(
  df            = my_data,
  calendar_file = calendar_path,
  date_col      = "date",
  value_col     = "value",

  calendar_mode = "basic",
  #   "none"     — ручной режим, т.е. задали так, чтобы сезонность убиралась без использования календаря по дням
  #   "basic"    — ручной режим, т.е. задали так, чтобы учитывалось только число рабочих дней
  #   "extended" — ручной режим, т.е. задали так, чтобы учитывались рабочие дни + праздники
  #   "auto"     — оценивает none/basic/extended и самостоятельно выбирает лучшее по информ.критерию (AIC)

  include_easter = TRUE,
  #   "TRUE"     - будет включен регрессор с Пасхой
  #   "FALSE"    - не будет включен регрессор с Пасхой

  transform_function = "none",
  #   "none"     — императивно задаем отсутствие дополнительных преобразований данных. Если вы работаете с инфляцией, и уже перевели темпы в накопленный лог-уровень, это нужный вариант
  #   "auto"     — X-13 сам решает, нужно ли преобразование. Подходит для данных в положительных уровнях
  #   "log"      — императивно задаем логарифмическое преобразование (если уверены, что оно нужно). Подходит для данных в положительных уровнях

  use_outliers    = TRUE,
  #   "TRUE"     - авто-поиск выбросов (AO/LS/TC)
  #   "FALSE"    - не даем искать выбросы. Этот вариант может пригодиться, если мы захотим сами задать модели выбросы, которые считаем нужными

  outlier_critical = NULL,

  method = "prefer_seats",
  #   "prefer_seats" — используется алгоритм SEATS, при неудаче происходит переход к X11
  #   "seats"        — только SEATS, при неудаче выдаст ошибку, если не хотим перехода к X11
  #   "x11"          — только X11

  forecast_months = 36,
  seasonality_alpha = 0.05,
  verbose = TRUE
)

# --- Вывод графика - SA vs. nonSA ---
ggplot(res$data, aes(x = date)) +
  geom_line(aes(y = original), linewidth = 1.0, colour = "black") +
  geom_line(aes(y = adjusted), linewidth = 0.5, colour = "red") +
  labs(title = "Сезонная корректировка", x = NULL, y = NULL)

# --- Сохранение ---
write.csv(res$data, "seasonally_adjusted_result.csv", row.names = FALSE)
# csv файл сохранится в папку, выбранную в качестве рабочей директории
# нужная колонка - adjusted

# --- Если понадобится - вывод дополнительных параметров результатов ---
res$seasonality_detected      # найдена ли сезонность вообще
res$qs_orig_evadj_pval        # p-value QS-теста (с поправкой на выбросы)
res$chosen_model              # none / basic / extended
res$transform                 # log / none
res$decomposition_method      # seats / x11 (x11 = когда SEATS не строится)
res$arima                     # выбранная ARIMA-модель
res$outliers                  # какие аутлаеры зашли
print(as.data.frame(res$comparison))   # таблица сравнения спецификаций
names(coef(res$best_model_object))     # регрессоры/аутлаеры финальной модели


# ============================================================================
# 2. ЗАМОРОЗКА СПЕЦИФИКАЦИИ (если хочется избежать эффекта "wagging tail")
# Нужна, чтобы при добавлении новой точки существенно не менялся исторический
# ряд. Делается в два шага: identify (определение спецификации) -> apply (применение)
# ============================================================================

# ----------------------------------------------------------------------------
# Шаг 1. Identify
# ----------------------------------------------------------------------------

sa_ru_identify(
  df            = my_data,
  calendar_file = calendar_path,

  series_id     = "inflprod",
  #   имя ряда   - если хотите, чтобы спецификация была сохранена для будущего применения (сохранится в папку в рабочей директории)
  #   NULL       - если не хотите, что спецификация сохранялась (тогда apply не будет работать) 

  cutoff_date   = "2025-12",
  #   дата, ПО которую (включительно) оцениваем и фиксируем спецификацию
  #   "2025-12"  — заморозить по декабрь 2025 (включительно)

  calendar_mode = "basic",
  #   "none"     — ручной режим, т.е. задали так, чтобы сезонность убиралась без использования календаря по дням
  #   "basic"    — ручной режим, т.е. задали так, чтобы учитывалось только число рабочих дней
  #   "extended" — ручной режим, т.е. задали так, чтобы учитывались рабочие дни + праздники
  #   "auto"     — оценивает none/basic/extended и самостоятельно выбирает лучшее по информ.критерию (AIC)
  
  include_easter = TRUE,
  #   "TRUE"     - будет включен регрессор с Пасхой
  #   "FALSE"    - не будет включен регрессор с Пасхой
  
  transform_function = "none",
  #   "none"     — императивно задаем отсутствие дополнительных преобразований данных. Если вы работаете с инфляцией, и уже перевели темпы в накопленный лог-уровень, это нужный вариант
  #   "auto"     — X-13 сам решает, нужно ли преобразование. Подходит для данных в положительных уровнях
  #   "log"      — императивно задаем логарифмическое преобразование (если уверены, что оно нужно). Подходит для данных в положительных уровнях
  
  use_outliers  = TRUE,
  #   "TRUE"     - авто-поиск выбросов (AO/LS/TC)
  #   "FALSE"    - не даем искать выбросы. Этот вариант может пригодиться, если мы захотим сами задать модели выбросы, которые считаем нужными
  
  method = "prefer_seats",
  #   "prefer_seats" — используется алгоритм SEATS, при неудаче происходит переход к X11
  #   "seats"        — только SEATS, при неудаче выдаст ошибку, если не хотим перехода к X11
  #   "x11"          — только X11
  
  forecast_months = 36,
  seasonality_alpha = 0.05,
  verbose = TRUE
)

# --- Если нужно будет посмотреть все сохраненные спецификации (они будут названы так, как вы задали в series_id) ---
print(sa_ru_list_specs())


# ----------------------------------------------------------------------------
# 2. Apply 
# ----------------------------------------------------------------------------

my_data_new <- readxl::read_xlsx("inflprod_nonSA.xlsx")   # тот же файл

res_apply <- sa_ru_apply(
  df            = my_data_new,
  calendar_file = calendar_path,
  series_id     = "inflprod"        # читает сохраненную спецификацию 
)

# --- Посмотреть весь скорректированный ряд ---
View(res_apply$data)

# Только НОВЫЕ точки после границы заморозки — их и дописываем в свою базу,
# историю не переписывая:
print(res_apply$new_points)

# --- График: весь ряд + новые точки (видно, как посезонились новые наблюдения) ---
# красные точки — новые наблюдения после границы заморозки; пунктир — сама граница
ggplot(res_apply$data, aes(x = date)) +
  geom_line(aes(y = original), linewidth = 1.0, colour = "black") +
  geom_line(aes(y = adjusted), linewidth = 0.5, colour = "red") +
  geom_vline(xintercept = as.Date(paste0(res_apply$spec$data_through, "-01")),
             linetype = "dashed", colour = "grey50") +
  labs(title = "Сезонная корректировка по фиксированной спецификации", x = NULL, y = NULL)

# --- Сохранение результата apply в csv ---
write.csv(res_apply$data, "seasonally_adjusted_result_apply.csv", row.names = FALSE)
# csv файл сохранится в папку, выбранную в качестве рабочей директории
# нужная колонка - adjusted
